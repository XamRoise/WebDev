const id = 100;

//equal to
if (id == 100) {
    console.log('Correct');
} else {
    console.log('Incorrect');
}

//not equal
if (id != 100) {
    console.log('Correct');
} else {
    console.log('Incorrect');
}

//equal to value & type
if (id === '100') {
    console.log('Correct');
} else {
    console.log('Incorrect');
}

if (id == '100') {
    console.log('Correct');
} else {
    console.log('Incorrect');
}